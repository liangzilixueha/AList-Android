class TempActivity : ComponentActivity() {


    val list = mutableStateListOf<String>()

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            AlistTheme {
                Column {
                    Btn()
                    LazyColumn {
                        items(list.size) {
